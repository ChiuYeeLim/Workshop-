# Packages and Libraries

# Packages are collections of R functions, data, and compiled code in a well-defined format, created to add specific functionality.

# There are 10,000+ user contributed packages and growing.

# The directories in R where the packages are stored are called the libraries.

# The terms package and library are sometimes used synonymously and there has been discussion amongst the community to resolve this.

# We only need to install a package once on our computer.

# However, to use the package, we need to load the library every time we start a new R/RStudio environment.

# You can think of this as installing a bulb versus turning on the light.
